            assert isinstance(config.index, config_module.IndexConfig)
            assert config.index.max_age_days == 10
            
            assert isinstance(config.pandoc, config_module.PandocConfig)
            assert config.pandoc.timeout == 15
            
            assert isinstance(config.parser, config_module.ParserConfig)
            assert config.parser.latex_attr == "copy-text"
            
            assert config.url_fallback_length == 20
            assert config.concurrency == 3
